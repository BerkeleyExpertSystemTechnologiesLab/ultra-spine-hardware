function lengths(x2,y2,z2,T2,G2,P2)
  t2 = sin(P2);
  t4 = sqrt(3.0);
  t5 = sin(T2);
  t6 = cos(G2);
  t7 = cos(P2);
  t8 = cos(T2);
  t9 = sin(G2);
  t12 = t5.*t7.*(3.0./4.0e1);
  t13 = t2.*t4.*t6.*(3.0./4.0e1);
  t14 = t2.*t8.*t9.*(3.0./4.0e1);
  t3 = abs(-t12-t13+t14+y2);
  t16 = t4.*(3.0./4.0e1);
  t17 = t2.*t5.*(3.0./4.0e1);
  t18 = t4.*t6.*t7.*(3.0./4.0e1);
  t19 = t7.*t8.*t9.*(3.0./4.0e1);
  t10 = abs(t16+t17-t18+t19-x2);
  t21 = t4.*t9.*(3.0./4.0e1);
  t22 = t6.*t8.*(3.0./4.0e1);
  t11 = abs(-t21-t22+z2+3.0./4.0e1);
  t15 = abs(-t12+t13+t14+y2);
  t20 = abs(-t16+t17+t18+t19-x2);
  t23 = abs(t21-t22+z2+3.0./4.0e1);
  t27 = t7.*t8;
  t28 = t2.*t5.*t9;
  t29 = t27+t28;
  t30 = t4.*t29.*(3.0./4.0e1);
  t24 = abs(t12-t14-t16+t30+y2);
  t32 = t4.*t5.*t6.*(3.0./4.0e1);
  t25 = abs(t22-t32+z2-3.0./4.0e1);
  t34 = t2.*t8;
  t35 = t5.*t7.*t9;
  t36 = t34-t35;
  t37 = t4.*t36.*(3.0./4.0e1);
  t26 = abs(t17+t19+t37+x2);
  t31 = abs(t12-t14+t16-t30+y2);
  t33 = abs(t22+t32+z2-3.0./4.0e1);
  t38 = abs(t17+t19-t37+x2);
  t42 = -t17+t18-t19+x2;
  t39 = abs(t42);
  t44 = t21+t22-z2+3.0./4.0e1;
  t40 = abs(t44);
  t41 = abs(t12+t13-t14+t16-y2);
  t43 = t39.^2;
  t45 = t40.^2;
  t46 = abs(-t12-t13+t14+t16+y2);
  t50 = t17+t18+t19-x2;
  t47 = abs(t50);
  t52 = t21-t22+z2-3.0./4.0e1;
  t48 = abs(t52);
  t49 = abs(-t12+t13+t14-t16+y2);
  t51 = t47.^2;
  t53 = t48.^2;
  t54 = abs(-t12+t13+t14+t16+y2);
  newLengths = zeros(8, 1);
  newLengths[1] = sqrt(t3.^2+t10.^2+t11.^2);
  newLengths[2] = sqrt(t15.^2+t20.^2+t23.^2);
  newLengths[3] = sqrt(t24.^2+t25.^2+t26.^2);
  newLengths[4] = sqrt(t31.^2+t33.^2+t38.^2);
  newLengths[5] = sqrt(t43+t45+t41.^2);
  newLengths[6] = sqrt(t43+t45+t46.^2);
  newLengths[7] = sqrt(t51+t53+t49.^2);
  newLengths[8] = sqrt(t51+t53+t54.^2);
  return newLengths;
end
